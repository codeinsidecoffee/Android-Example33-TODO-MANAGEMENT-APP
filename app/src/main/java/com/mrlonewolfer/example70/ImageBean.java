package com.mrlonewolfer.example70;

public class ImageBean {

    int imgId;
    String imgName;

    public ImageBean(int imgId) {
        this.imgId = imgId;

    }

    public int getImgId() {
        return imgId;
    }

    public void setImgId(int imgId) {
        this.imgId = imgId;
    }

    public String getImgName() {
        return imgName;
    }

    public void setImgName(String imgName) {
        this.imgName = imgName;
    }
}
